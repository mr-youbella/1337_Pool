int	ft_atoi(char *str)
{
	int	i;
	int	c;
	int	r;

	i = 0;
	c = 0;
	r = 1;
	while ((str[i] <= 15 && str[i] >= 9) || str[i] == 32)
		i++;
	while ((str[i] == '-' || str[i] == '+'))
	{
		if (str[i] == '-')
			r *= -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		c = c * 10 + str[i] - 48;
		i++;
	}
	return (c * r);
}
