#include <unistd.h>

void	f(char c)
{
	write(1, &c, 1);
}

void	ft(int a, int b)
{
	f(48 + a / 10);
	f(48 + a % 10);
	f(' ');
	f(48 + b / 10);
	f(48 + b % 10);
	if (a != 98)
	{
		f(',');
		f(' ');
	}
}

void	ft_print_comb2(void)
{
	int	x;
	int	y;

	x = 0;
	while (x <= 99)
	{
		y = x + 1;
		while (y <= 99)
		{
			ft(x, y);
			y++;
		}
		x++;
	}
}
